package com.hugo.primeiraSemana;

public class OperadoreUnarios {
    public static void main(String[] args) {
        int x = 5;
        x++; // agora x é igual a 6

        int y = 10;
        y--; // agora y é igual a 9


        boolean estaChovendo = true;
        boolean naoEstaChovendo = !estaChovendo; // agora naoEstaChovendo é igual a false

        int z = 7;
        int w = -z; // agora w é igual a -7



    }

}
