package com.hugo.segundaSemana.condicional;

public class CondicionalComposta {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;

        if (x > y) {
            System.out.println("x é maior do que y");
        } else {
            System.out.println("y é maior do que x");
        }

    }
}
